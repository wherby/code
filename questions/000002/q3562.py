from functools import cache
class Solution:
    def maxProfit(self, n: int, present: List[int], future: List[int], hierarchy: List[List[int]], budget: int) -> int:
        g = [[] for _ in range(n)]
        for a,b in hierarchy:
            a,b = a-1,b-1 
            g[a].append(b)
        
        def dfs(a):
            dp = [[0]*(budget+1) for _ in range(2)]
            for b in g[a]:
                tp = dfs(b)
                for i in range(budget,-1,-1):
                    for j in range(i+1):
                        dp[0][i] = max(dp[0][i],dp[0][i-j] + tp[0][j])
                        dp[1][i] = max(dp[1][i],dp[1][i-j] + tp[1][j])
            dp2 = [[0]*(budget+1) for _ in range(2)]
            for i in range(budget,-1,-1):
                if i >=present[a]:
                    dp2[0][i] = max(dp[0][i], dp[1][i-present[a]] + future[a] - present[a])
                else:
                    dp2[0][i] = dp[0][i]
                if i >= present[a]//2:
                    dp2[1][i] = max(dp[0][i],dp[1][i-present[a]//2] +future[a] - present[a]//2 )
                else:
                    dp2[1][i] = dp[0][i]
            return dp2
        return dfs(0)[0][budget]


