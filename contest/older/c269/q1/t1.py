class Solution(object):
    def targetIndices(self, nums, target):
        """
        :type nums: List[int]
        :type target: int
        :rtype: List[int]
        """
        nums.sort()
        res =[]
        for i,a in enumerate(nums):
            if a ==target:
                res.append(i)
        return res